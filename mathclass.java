public class mathclass {
    public static void main(String[] args) {
        System.out.println(Math.min(3,5));
        System.out.println(Math.max(2,7));
        System.out.println(Math.sqrt(5));
        System.out.println(Math.random());//generates a random number between 0 and 1;
        System.out.println(Math.random()*100);// generates a random number between 0 and 100
        System.out.println((int)(Math.random()*100));//generates a random integer number between 0 and 100
    }
}
